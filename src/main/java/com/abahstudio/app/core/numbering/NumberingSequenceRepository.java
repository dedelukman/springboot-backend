package com.abahstudio.app.core.numbering;

import jakarta.persistence.LockModeType;
import org.springframework.data.jpa.repository.JpaRepository;
import org.springframework.data.jpa.repository.Lock;
import org.springframework.data.jpa.repository.Query;
import org.springframework.data.repository.query.Param;
import org.springframework.stereotype.Repository;

import java.util.Optional;

@Repository
public interface NumberingSequenceRepository extends JpaRepository<NumberingSequence, Long> {

    Optional<NumberingSequence> findBySequenceCode(String sequenceCode);

    @Lock(LockModeType.PESSIMISTIC_WRITE)
    @Query("SELECT ns FROM NumberingSequence ns WHERE ns.sequenceCode = :sequenceCode")
    Optional<NumberingSequence> findBySequenceCodeWithLock(@Param("sequenceCode") String sequenceCode);
}
